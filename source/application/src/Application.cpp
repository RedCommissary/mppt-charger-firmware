/********************************************************************************
 * Class Application
 ********************************************************************************/

void Application::Init() {
    Clock::Init();
    Led::Init();

    StartLowSpeedProcessing();
    StartHighSpeedProcessing();

    Led::On(Led::Type::on);
}

/********************************************************************************
 * High speed processing #1
 ********************************************************************************/

void sTim2::handler() {
    Timer::ResetFlagTim2();
}

/********************************************************************************
 * Low speed processing #1
 ********************************************************************************/

void sTim3::handler() {
    Timer::ResetFlagTim3();
    Led::Toggle(Led::Type::status);
}